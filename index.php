					<p class="lead bottom0 wow bounceInUp">					</p>

					<h2>ABOUT SASE:</h2>
						<p>
						   The Society of Asian Scientists and Engineers (SASE) was founded in November 2007 to help Asian heritage scientific and engineering professionals achieve their full potential. Organizations existed for other affinity groups – The National Society of Black Engineers, the Society of Hispanic Professional Engineers, and the Society of Women Engineers – and there was a need for a similar organization where students representing all of the pan Asian cultures could connect and support each other.
						    
						    SASE is a non-profit 501(c)3 organization, and contributions to SASE are tax deductible.
						</p>

					<h2>OUR MISSION:</h2>
						<p>
						   Our mission statement is to prepare Asian heritage scientists, engineers and technologists for success in the global business world, celebrate diversity on campuses and in the workplace, and to provide opportunities for members to make contributions to their local communities.
						    SASE membership is open to men and women of all ethnic backgrounds.
						    
						    Click here if you are interested in joining SASE National Membership (optional). It’s free!
						</p>

					<h2>OUR HISTORY:</h2>
						<p>
						    The Procter &amp; Gamble Company saw the opportunity to improve the recruiting, retention and development of Asian heritage scientists and engineers via a nationally recognized technical organization with student chapters on college campuses. In 2007-08 PG provided the initial membership contribution to establish SASE as a 501(c)3 organization.
						</p>            
